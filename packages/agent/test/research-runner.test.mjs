function runCli(args, { env, input } = {}) {
  return new Promise((resolve) => {
    const child = spawn(process.execPath, ['dist/cli.js', ...args], {
      cwd: new URL('..', import.meta.url),
      env: { ...process.env, ...env },
      stdio: ['pipe', 'pipe', 'pipe'],
    });

    let stdout = '';
    let stderr = '';
    child.stdout.on('data', (chunk) => {
      stdout += chunk;
    });
    child.stderr.on('data', (chunk) => {
      stderr += chunk;
    });
    child.on('close', (code) => {
      resolve({ code, stdout, stderr });
    });

    if (input !== undefined) {
      child.stdin.end(input);
    } else {
      child.stdin.end();
    }
  });
}

describe('research-run CLI', () => {
  it('accepts --draft JSON and prints a completed JSON result', async () => {
    const result = await runCli(
      [
        'research-run',
        '--prompt',
        'Research a simple BTC trend-following strategy.',
        '--draft',
        JSON.stringify(makeDraft()),
      ],
      {
        env: {
          NODE_OPTIONS: '--import ./test/mock-cli-fetch.mjs',
          TRASEQ_AGENT_TEST_VALIDATION: 'ok',
          TRASEQ_API_KEY: 'test-key',
          TRASEQ_BASE_URL: 'https://api.test.local',
        },
      },
    );

    assert.equal(result.code, 0, result.stderr);
    const parsed = JSON.parse(result.stdout);
    assert.equal(parsed.status, 'completed');
    assert.equal(parsed.rounds[0].backtest.id, 'bt-1');
  });

  it('rejects the --rounds flag because the CLI is single-round', async () => {
    const result = await runCli(
      [
        'research-run',
        '--prompt',
        'Research a simple BTC trend-following strategy.',
        '--rounds',
        '3',
        '--draft',
        JSON.stringify(makeDraft()),
      ],
      {
        env: {
          NODE_OPTIONS: '--import ./test/mock-cli-fetch.mjs',
          TRASEQ_AGENT_TEST_VALIDATION: 'ok',
          TRASEQ_API_KEY: 'test-key',
          TRASEQ_BASE_URL: 'https://api.test.local',
        },
      },
    );

    assert.notEqual(result.code, 0);
    assert.match(result.stderr, /single-round by design/);
  });

  it('accepts --stdin draft JSON and exits non-zero on validation failure', async () => {
    const result = await runCli(
      [
        'research-run',
        '--prompt',
        'Research a simple BTC trend-following strategy.',
        '--stdin',
      ],
      {
        input: JSON.stringify(makeDraft()),
        env: {
          NODE_OPTIONS: '--import ./test/mock-cli-fetch.mjs',
          TRASEQ_AGENT_TEST_VALIDATION: 'fail',
          TRASEQ_API_KEY: 'test-key',
          TRASEQ_BASE_URL: 'https://api.test.local',
        },
      },
    );

    assert.notEqual(result.code, 0);
    const parsed = JSON.parse(result.stdout);
    assert.equal(parsed.status, 'failed');
    assert.equal(parsed.stopReason, 'validation_failed');
    assert.equal(parsed.rounds[0].validation.valid, false);
  });
});
